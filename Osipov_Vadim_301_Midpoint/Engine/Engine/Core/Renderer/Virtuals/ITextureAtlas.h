#pragma once

class ITextureAtlas
{
public:
	virtual ~ITextureAtlas() { }

	//---------------------------------------------------------------------------------------------
	// Get first GID of first image in that atlas.
	//---------------------------------------------------------------------------------------------
	virtual int GetFirstGID() const = 0;

	//---------------------------------------------------------------------------------------------
	// Get last GID of last image in the atlas
	//---------------------------------------------------------------------------------------------
	virtual int GetLastGID() const = 0;

	//---------------------------------------------------------------------------------------------
	// Get spacing in pixels between tiles in atlas
	//---------------------------------------------------------------------------------------------
	virtual int GetImageSpacing() const = 0;

	//---------------------------------------------------------------------------------------------
	// Get margin in pixels in current atlas
	//---------------------------------------------------------------------------------------------
	virtual int GetImageMargin() const = 0;
};
